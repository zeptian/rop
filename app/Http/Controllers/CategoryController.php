    public function subcategory(Request $request)
    {
        $status = false;
        $message = 'failed';
        $data = null;
        $category = $request->input('category');
        $sub = Category::where('parent_id', $category)->get();
        if ($sub) {
            $status = true;
            $data = $sub;
            $message = 'success';
        }
        return response()->json(['status' => $status, 'message' => $message, 'data' => $data]);
    }

